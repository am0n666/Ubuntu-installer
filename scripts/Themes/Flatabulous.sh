#!/usr/bin/env bash

echo "Installing Flatabulous"
																																																																																																																							
mkdir -p ~/.themes && wget -O /tmp/Flatabulous-master.zip https://github.com/anmoljagetia/Flatabulous/archive/master.zip
cd ~/.themes && unzip -o /tmp/Flatabulous-master.zip
rm /tmp/Flatabulous-master.zip
